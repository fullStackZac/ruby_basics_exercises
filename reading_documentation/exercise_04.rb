Exercise: In the previous exercise, you learned that the while loop returns nil 
unless break is used. Locate the documentation for break, and determine what value
break sets the return value to for the while loop.

The result of a while loop is nil unless break is used to supply a value is how the docs describe the break 
keyword affecting the return value.  This means the value is supplied by the programmer via the break
or the user via the choice/input made via the break keyword.
